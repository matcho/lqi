<div class="bio" data-idx="32" style="display: none;">
	<h2>SEGUIN Teddy</h2>
	<p>Formé à L'Ecole Nationale Supérieure de la Photographie d'Arles, Teddy Seguin se consacre dans un premier temps à la photographie de reportage dans laquelle il explore les univers clos. Son travail est régulièrement publié dans la presse nationale et internationale. <br />
     <br />A partir de 2010, il commence à réaliser des séries plus personnelles avec toujours cet intérêt pour l'insularité. La mondialisation réduit à présent les frontières qui séparent encore les "insulaires" du reste du monde. Pourtant, l'isolement qu'il soit géographique, social ou culturel persiste et façonne encore des caractères forts et singuliers sur lequel s'attarde ses dernières séries comme Outport.
	<p>
