<div class="bio" data-idx="30" style="display: none;">
	<h2>PHELPS Sam Claudio</h2>
	<p>Sam Phelps est un photographe australien établi à Dakar au Sénégal, travaillant principalement en Asie du Sud et en Afrique de l'Ouest. Ses premiers pas en photographie documentaire ont consisté en un trajet par la route entre Mumbaï et Paris en 2007. Tout en voyageant, il préparait ainsi un portfolio de ses travaux à travers l'Inde, le Pakistan, l'Afghanistan, l'Iran et la Syrie. Il s'est ensuite installé au Pakistan en 2011-2012 pour des projets personnels.  <br />
     <br />Ses études aux Beaux-arts de Sydney, la création de publicités indonésiennes dans la très vivante Jakarta, ainsi que de nombreuses années d'expérience dans l'industrie de la mode à Paris ont marqué son style et ont influencé sa pratique de la photographie documentaire.
	<p>
