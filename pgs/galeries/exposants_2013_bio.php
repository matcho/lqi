	<p>Seung-Hwan Oh travaille et vit à Séoul, où il est né et a grandi jusqu'à ce qu'il déménage à New York. Là-bas, il a étudié le cinéma et la photographie au City University of New York Hunter College. Se situant dans une approche totalement innovante, sa source d'inspiration provient de son intérêt sans borne pour l'application de la théorie et des découvertes scientifiques à la photographie. Son oeuvre la plus récente a été exposée au Zaha Museum, et il travaille actuellement sur l'utilisation de la croissance bactérienne comme moyen d'explorer le caractère éphémère des choses tout comme les limitations matérielles de la photographie.
	<p>
