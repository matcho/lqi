<div class="bio" data-idx="33" style="display: none;">
	<h2>SOUSSI Johann</h2>
	<p>Johann Soussi est né en 1978 à Paris où il vit et travaille. Professeur de mathématiques de formation, il enseigne à l'Éducation Nationale sept années avant de se consacrer, en 2010, entièrement à la photographie.  <br />
     <br />Photographe autodidacte, il pose sur Paris un regard authentique et passionné qui donne à voir des univers à la fois familiers et insolites. Johann SOUSSI se révèle au grand public en 2010 avec sa première série Allerretour aujourd'hui conservée à la Bibliothèque nationale de France. En 2012, son nouveau travail très remarqué Garde à vous est exposé dans différents salons, galeries et festivals et fait l'objet de plusieurs installations hors-les-murs.
	<p>
</div>
<div class="bio" data-idx="34" style="display: none;">
	<h2>TAHTAH Marwan</h2>
	<p> Né le 17 septembre 1981, Marwan Tahtah a étudié l'architecture à l'Amliye Institute. Parallèlement, il a participé à plusieurs ateliers de photographie, dont le célèbre atelier du Centre culturel russe. Sa passion pour la photographie prit le dessus et il poursuivit ses études tout en faisant son service militaire. Il étudia également à l'Altaraki Institute. Marwan commença sa carrière de photographe professionnel au journal Alsharek, en 2001. Il y resta jusqu'au lancement du journal Al Akhbar en 2006.  <br />
     <br />Il a participé à plusieurs expositions, dont l'exposition des photographes syndiqués de 2004, où il a reçu le 1er prix pour sa photo. Sa première exposition personnelle a eu lieu en 2011. Son travail sur le terrain a dépassé les frontières et l'a emmené là où très peu de personnes sont allées, en raison de la situation politique explosive ou des guerres qui éclatent dans la région.
	<p>
</div>
<div class="bio" data-idx="35" style="display: none;">
	<h2>TAKIZAMA Akiko</h2>
	<p> Akiko Takizawa est née en 1971 dans la ville de Fukuoka au Japon et travaille et vit à Londres. Elle a étudié au Royal College of Art à Londres de 2006 à 2009 où elle a obtenu un MA des Beaux Arts. Ses plus récentes expositions solo furent Apple Room à la galerie de Tosei à Tokyo en 2013 et Over the Parched filed à la fondation anglo-japonaise Daiwa, à Londres en 2012.  <br />
