<div class="bio" data-idx="29" style="display: none;">
	<h2>PARISI Tony</h2>
	<p>Tony Parisi est né à Minittello, en Sicile. Peu de temps après sa naissance, ses parents immigrent dans le sud de la France. Son père, artisan carreleur, lui offre son premier appareil photographique, un Rolleiflex, à l'âge de 10 ans avec lequel il réalise ses premiers portraits. En 2001, il s'installe à Paris et travaille pour plusieurs agences de mannequins en France et à l'étranger, et réalise divers catalogues de mode. S'éloignant de plus en plus des techniques de prise de vue de la photographie de mode, il se lance alors dans des travaux plus personnels.
	<p>
