<div class="bio" data-idx="31" style="display: none;">
	<h2>ROCHA Sandra</h2>
	<p>Sandra Rocha est une photographe née aux Açores en 1974. Elle vit actuellement à Paris. Après une formation initiale en photographie à Ar.Co (Centre d'arts et communication visuel, Lisbonne), elle a obtenue en 2007 son Master en Histoire de l'Art contemporain à l'Université Nova de Lisbonne. Ses images sont présentes dans plusieurs collections: Fondation EDP, Fondation Gulbenkian, Foire de Art de Vigo, Musée Fernando Pessoa, Gouvernement de la Cantabria, Gouvernement Régional des Açores.  <br />
     <br />Actuellement, son projet Waterline, constitué de 12 grands formats et d'une installation vidéo fait l'objet d'une exposition individuelle au Musée d'Art Moderne et Contemporain de Santander.
