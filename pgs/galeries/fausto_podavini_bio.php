<h2>Fausto Podavini</h2>

<?php if ($langue == 'fr'): ?>
<p>
	Fausto Podavini est né à Rome, où il vit et travaille encore aujourd'hui. Il a obtenu un master en reportage de la Photographic Academy John Kaverdash, à Milan. 
</p>
<p>
Après des débuts dans la photographie de studio, il décide de se concentrer sur le reportage en tant que freelance et réalise des reportages en Italie, au Pérou, au Kenya et en Ethiopie. En plus du prix World Press Photo en 2013, il a remporté le Sony Awards et le PX en 2013. En 2012, il a reçu le PDN et en 2011, il a remporté le World Report Awards et le prix du photographe de l'année FIOF. Son travail a été exposé dans plusieurs villes.
</p>

