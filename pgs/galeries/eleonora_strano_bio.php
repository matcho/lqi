<h2>Eleonora Strano</h2>

<?php if ($langue == 'fr'): ?>
<p>
	Eleonora Strano est une photographe indépendante basée en France. 
Après des études de physique, de linguistique et de photographie, elle travaille comme attachée culturelle et professeur de français. 
</p>
<p>
En 2009, elle gagne le prix international de photojournalisme de l’Emi-Cfd et y étudie durant 7 mois. 
Ses travaux ont été exposés en France, en Italie et en Allemagne, à l’Hôtel de Sauroy, “La maison des Photographes” ou encore Paris Photo Off. 
</p>
<p>
	Son travail a également été projeté au BAL, en juin 2011 et au Musée de la Photographie de Mougins “Se dérober”, janvier-juin 2013. 
Elle se consacrera, dans les mois à venir, à la diffusion de ses derniers travaux sur la jeunesse turkmène.
</p>
<?php endif; ?>

