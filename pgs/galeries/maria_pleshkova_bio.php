<h2>Maria Pleshkova</h2>

<?php if ($langue == 'fr'): ?>
<p>
	Maria Pleshkova est photographe documentaire. Elle est née en 1986 à Moscou où elle a étudié le photojournalisme à la Moscow State University en 2009-2010.
</p>
<p>
En parallèle, elle participait au workshop de Sergey Maximishin à l'école des arts visuels de Moscou, puis a été choisie, en 2012, pour participer au workshop d'Eddie Adams à New York. Maria a remporté quelques récompenses notamment aux concours du China Press Photo et des Jeunes photographes russes. 
</p>
<p>
	Elle a également été finaliste aux prix de Inge Morath et de photographie internationale. Ses travaux ont été exposés en Russie, en France et en Espagne.
</p>
