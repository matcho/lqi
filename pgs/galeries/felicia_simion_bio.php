Sa première exposition solo, In the pursuit of light, a eu lieu en septembre à la galerie R K Burt de Londres, où elle en a profité pour présenter son livre de photographie du même nom.
</p>
<p>
	Elle a aussi participé à des expositions réunissant plusieurs photographes : Travel Photographer of the Year (en 2013 et en 2014), The Royal Geographical Society, Londres ;»Fact and fantasy», The Kiernan Gallery, Virginie ; «Visible White», Studio Marangoni Gallery, Florence ; «IDentity», The Center for Fine Art Photography, Colorado; «I’ll be your mirror: The Portrait», 1650 Gallery, Los Angeles; «Life Framer», en Suisse et au Royaume Uni ; “Discover Europe”, Grand Gala, Varsovie ; les Sony World Photography Awards 2012, Somerset House, Londres.
</p>
