<h2>Felicia Simion</h2>

<?php if ($langue == 'fr'): ?>
<p>
	Photographe de 20 ans d’origine roumaine, « excentrique » de la famille, Felicia est amoureuse des arts depuis qu’elle est enfant. Elle fait de la photographie depuis ses 13 ans, âge auquel elle a commencé à s’intéresser aux beaux-arts et à l’art du portrait.
</p>
<p>
