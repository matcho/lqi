<?php if ($langue == 'en'): ?>
<p>
	She is a 20-year-old photographer from Romania, oddball of the family, art lover since she was a weeping little girl.
She has been carrying a camera around her neck from the age of 13, when fine art and portraiture started to intrigue her.
</p>
<p>
	She has received awards in international competitions such as: Prix de la Photographie, Sony World Photography Awards, Travel Photographer of the Year, The Irish Times Photography Awards, Fotorama contest, FRAMED awards etc.
Her first solo exhibition, In the pursuit of light, took place in September at the R K Burt Gallery in London, where she also launched a photography book with the same title. 
</p>
<p>
	Her works have been included in group exhibitions at The Royal Geographical Society, London (Travel Photographer of the Year 2013 and 2014), The Kiernan Gallery, Virginia («Fact and fantasy» exhibition), Studio Marangoni Gallery, Florence («Visible White» show), The Center for Fine Art Photography, Colorado («IDentity» exhibition), 1650 Gallery, Los Angeles («I’ll be your mirror: The Portrait» exhibition), Life Framer exhibition, in Switzerland and UK, “Discover Europe” show Grand Gala, Warsaw, Somerset House, London (Sony World Photography Awards 2012) etc.
</p>
